# print(x[len(x)-1]) = asa se afiseaza caracterul de la un index specificat
litera = input('Introdu un string: ')
ultimul_index = len(litera)-1
print(ultimul_index) # aici iti afiseaza un nr, nr-ul pozitiei ultimului caracter
print(litera[len(litera)-1])# aici iti afiseaza carcterul de pe ultima pozitie
print(litera[ultimul_index])
print(litera[0:])# aici iti afiseaza tot string-ul
